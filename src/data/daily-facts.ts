export function getDailyFact(childId: string, date = new Date()): DailyFact {
  const daySeed =
    date.getFullYear() * 1000 + date.getMonth() * 31 + date.getDate();
  let hash = 0;
  const key = `${childId}-${daySeed}`;
  for (let i = 0; i < key.length; i++) {
    hash = (hash * 31 + key.charCodeAt(i)) % DAILY_FACTS.length;
  }
  return DAILY_FACTS[hash]!;
}
