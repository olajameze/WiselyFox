export type DailyFact = {
  text: string;
  subject: string;
  subjectLabel: string;
  skill: string;
  detail: string;
  example?: string;
  learnHref: string;
  learnLabel: string;
};

const SUBJECT_LABELS: Record<string, string> = {
  maths: "Mathematics",
  reading: "Reading",
  coding: "Coding",
  money: "Money skills",
  "study-skills": "Study skills",
  "career-skills": "Career & business",
};

function fact(
  text: string,
  subject: string,
  skill: string,
  detail: string,
  learnHref: string,
  learnLabel: string,
  example?: string,
): DailyFact {
  return {
    text,
    subject,
    subjectLabel: SUBJECT_LABELS[subject] ?? subject,
    skill,
    detail,
    example,
    learnHref,
    learnLabel,
  };
}

